    // Retrieve form data
    $fullname = $_POST['fullname'];
    $email = $_POST['email'];
    $phone = $_POST['phone'];
    $destination = $_POST['destination'];

    // Insert data into table
    $insert_sql = "INSERT INTO details (fullname, email, phone, destination) VALUES ('$fullname', '$email', '$phone', '$destination')";

